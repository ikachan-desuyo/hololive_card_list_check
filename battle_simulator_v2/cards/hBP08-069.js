/**
 * 一伊那尓栖 (hBP08-069) 紫・Debut・HP100（#EN #Myth #絵 #海）
 *
 * コラボエフェクト「夏の訪れ」:
 *   自分のステージに#絵を持つホロメンが3人以上いるなら、自分のデッキを1枚引く。
 *   → 自分のステージ上のホロメン（トップのカード）のうち #絵 タグ保持を数え、
 *     3人以上なら1枚ドロー（条件を満たさなければ何もしない）。
 *
 * アーツ「うまくいったでしょ、もっといけるよ」(30): 効果なし（テキスト効果なし、素点のみ）。
 *
 * 保留: なし。
 */
export default {
  number: 'hBP08-069',

  collabEffect: {
    name: '夏の訪れ',
    *run(ctx) {
      const count = ctx.holomems('self', (e) => ctx.hasTag(e.top, '絵')).length;
      if (count >= 3) {
        ctx.log(`#絵 を持つホロメンが${count}人いるため1枚ドロー`);
        ctx.draw(1);
      } else {
        ctx.log(`#絵 を持つホロメンが${count}人（3人未満）のため効果なし`);
      }
    },
  },
};
