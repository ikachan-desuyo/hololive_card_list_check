/**
 * ぽこべぇ (hSD06-012) サポート・マスコット
 *
 * [サポート効果] このマスコットが付いているホロメンのHP+10。
 *   → attached.hpPlus で常時 +10。
 *
 * ◆〈風真いろは〉に付いていたら能力追加
 *   このマスコットが付いているホロメンのHP+20。
 *   → 付け先が〈風真いろは〉なら追加で +20（合計 +30）。
 *
 * マスコットは、自分のホロメン1人につき1枚だけ付けられる（マスコット共通制限。エンジンが既定で処理）。
 */
export default {
  number: 'hSD06-012',
  attached: {
    hpPlus(holomem) {
      let n = 10;
      if (holomem.stack[0].name === '風真いろは') n += 20;
      return n;
    },
  },
};
