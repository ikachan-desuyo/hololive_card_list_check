/**
 * Thorn (hBP07-104) サポート・ツール
 * このツールが付いている〈エリザベス・ローズ・ブラッドフレイム〉のアーツ+20。
 *   （※基本効果は付いているホロメンが〈エリザベス・ローズ・ブラッドフレイム〉の場合のみ）
 * ◆2ndの〈エリザベス・ローズ・ブラッドフレイム〉に付いていたら能力追加:
 *   このホロメンのHPが減っているなら、このホロメンのアーツ+20。
 * ツールは、自分のホロメン1人につき1枚だけ付けられる（デフォルトのツール制限）。
 */
export default {
  number: 'hBP07-104',
  attached: {
    artsPlus(holomem) {
      const top = holomem.stack[0];
      if (top.name !== 'エリザベス・ローズ・ブラッドフレイム') return 0;
      let n = 20;
      // 2nd で HP が減っている（ダメージを受けている）なら追加 +20
      if (top.bloomLevel === '2nd' && holomem.damage > 0) {
        n += 20;
      }
      return n;
    },
  },
};
