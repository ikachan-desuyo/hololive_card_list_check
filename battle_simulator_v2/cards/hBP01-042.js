/**
 * 兎田ぺこら (hBP01-042) 緑・1st・HP120（#JP #3期生 #ケモミミ）
 * アーツ「白い砂浜と兎少女」(40): 効果なし（プレーンアーツ）。
 * アーツ「きｔらあああ」(50+):
 *   サイコロを１回振れる：出た目の数１につき、このアーツ+10。
 *   →「振れる」=任意（振らない選択も可）。出目×10をアーツ加算。
 */
export default {
  number: 'hBP01-042',
  arts: {
    'きｔらあああ': {
      *run(ctx) {
        const ok = yield ctx.confirm('サイコロを1回振りますか？（出た目×10このアーツ+）', '振る', '振らない');
        if (!ok) return;
        const v = ctx.rollDice();
        ctx.addArtBonus(v * 10, `サイコロの目${v}×10`);
      },
    },
  },
};
