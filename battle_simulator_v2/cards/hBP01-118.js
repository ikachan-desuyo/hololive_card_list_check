/**
 * あん肝 (hBP01-118) サポート・マスコット
 *
 * [サポート効果] このマスコットが付いているホロメンのHP+10。
 *   → attached.hpPlus で常時 +10。
 *
 * ◆〈ときのそら〉に付いていたら能力追加:
 *   このマスコットが付いているホロメンがアーツを使う時、このマスコットを白エールとしても扱う。
 *    → attached.cheerSupply で実装（アーツ使用時に白エール1個を擬似供給）。
 *
 * マスコットは、自分のホロメン1人につき1枚だけ付けられる（マスコット共通制限。エンジンが既定で処理）。
 */
export default {
  number: 'hBP01-118',
  attached: {
    hpPlus() {
      return 10;
    },
    // ◆〈ときのそら〉に付いていたら能力追加: アーツを使う時、このマスコットを白エールとしても扱う（擬似エール供給）
    cheerSupply(holomem) {
      return holomem.stack[0].name === 'ときのそら' ? [{ color: '白' }] : [];
    },
  },
};
