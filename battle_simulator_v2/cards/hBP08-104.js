/**
 * けはい (hBP08-104) サポート・ツール
 *
 * [サポート効果]
 * ◆〈水宮枢〉に付いていたら能力追加:
 *   ■[センターポジション・コラボポジション限定]相手のセンターホロメンのバトンタッチに必要な無色+1。
 *   ■このホロメンのBloomレベルが上がった時、自分のデッキを1枚引く。
 * ツールは、自分のホロメン1人につき1枚だけ付けられる
 *   （ツール1人1枚の上限はエンジンの _canAttachSupport が既定で処理。付け先制限は無いので attachRule 不要）。
 *
 * 実装メモ:
 * - 「Bloomレベルが上がった時、自分のデッキを1枚引く」は triggers.onBloom（このツールが付いた
 *   ホロメンがBloomした時に engine が発火）。ホストが〈水宮枢〉のときだけ能力が追加されるので、
 *   ctx.sourceHolomem の最上段カード名で判定する。ホロメンは1ターン1回しかBloomできないため
 *   「Bloomレベルが上がった時」は自然に満たされる。
 *
 * 保留: 「[センターポジション・コラボポジション限定]相手のセンターホロメンのバトンタッチに必要な
 *   無色+1」は未実装（2026-06 再評価でも保留継続）。
 *   この効果は「装着ツールが付いたホストが前衛(センター/コラボ)に居る間、常時、相手センターの
 *   バトンタッチ必要無色を+1する」という常時アウラ。現行エンジンの batonCostReduce 機構を確認したが、
 *   effects.batonCostReduction(holomem, ownerIdx) が参照するのは
 *     ①ターン修正 kind:'batonCostReduce'（イベントで1回 push する能動型。hBP08-004 推しスキル等）と
 *     ②評価対象ホロメン自身の attachments の batonCostReduceAttached（その装着が「評価対象に」付いている時のみ。
 *       hBP03-111 等）の2つだけ。
 *   本カードはツールが「自分の」ホロメンに付くのに、コストを増やす対象は「相手の」センターなので
 *   ②では届かず、①は常時アウラには使えない（前衛離脱・装着剥がれ時の後始末や毎ターン再付与の機構が無い）。
 *   必要なのは「盤面を走査して装着カードが相手センターのバトンコストにデルタを与える常時アウラ」フック
 *   （auraDamageDelta / artsCostReduceAura の baton 版）。これは core(system.js/engine.js) 側の新機構で、
 *   発明禁止のため保留を維持する。なお下記 onBloom（〈水宮枢〉に付いていた時のドロー）は実装済み。
 */
export default {
  number: 'hBP08-104',
  triggers: {
    *onBloom(ctx) {
      const host = ctx.sourceHolomem; // このツールが付いた（Bloomした）ホロメン
      const top = host?.stack?.[0];
      if (!top) return;
      // ◆〈水宮枢〉に付いていたら能力追加
      if (top.name !== '水宮枢') return;
      ctx.draw(1);
    },
  },
};
