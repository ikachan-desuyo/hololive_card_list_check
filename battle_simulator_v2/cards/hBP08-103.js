/**
 * ホロマント (hBP08-103) サポート・ツール
 *
 * [サポート効果]
 *  ■このツールが付いているホロメンのHP+50。
 *    → attached.hpPlus で常時 +50。
 *  ■このツールが付いているホロメンがダウンした時、自分のライフ-1。
 *    → triggers.onDown（ダウンしたホロメン＋その装着カードで発火。ctx.sourceHolomem=ダウンしたホロメン、
 *      ctx.player=このツールの持ち主）。持ち主の lifeDamage を +1 して、ダウンによる通常のライフダメージとは
 *      別に追加で1ライフ失わせる（hBP01-014 と同じ機構。アーカイブ前に発火し、その後 _checkTiming の
 *      11.5 ライフダメージ処理に合流する）。
 *
 * ※ ツールは自分のホロメン1人につき1枚（エンジン既定の装着上限。attachRule 不要）。
 *
 * 保留: なし（HP+50・ダウン時ライフ-1 とも全文実装）。
 */
export default {
  number: 'hBP08-103',

  attached: {
    // このツールが付いているホロメンのHP+50
    hpPlus() { return 50; },
  },

  triggers: {
    // このツールが付いているホロメンがダウンした時、自分（持ち主）のライフ-1
    *onDown(ctx) {
      ctx.player.lifeDamage += 1;
      ctx.log('ホロマント: 付いているホロメンがダウン → 自分のライフ-1');
    },
  },
};
